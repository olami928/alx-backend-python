#!/usr/bin/env python3
"""
This module defines a type-annotated function to add two floats.
"""


def add(a: float, b: float) -> float:
    """
    Return the sum of two floats.

    Args:
        a (float): The first float to add.
        b (float): The second float to add.

    Returns:
        float: The sum of a and b.
    """
    return a + b
